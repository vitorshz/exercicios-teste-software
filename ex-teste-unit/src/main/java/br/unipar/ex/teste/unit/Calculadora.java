
package br.unipar.ex.teste.unit;

public class Calculadora {
    
    public double somarValores(double v1, double v2){
        return v1+v2;
    }
    public double subtrairValores(double v1, double v2){
        return v1-v2;
    }
    public double multiplicarValores(double v1, double v2){
        return v1*v2;
    }
    public double dividirValores(double v1, double v2){
        return v1/v2;
    }
}
